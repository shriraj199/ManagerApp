@login_required
def maintenance_view(request):
    from .models import InviteCode, PaymentProof, RentPaymentProof, SocietyMaintenanceSettings, RentalChargeSettings
    from django.utils import timezone
    import re
    from datetime import datetime

    is_rental = (request.user.role == 'resident' and request.user.resident_role == 'rental')
    society_name = request.user.society_name
    
    # ─── Context Data ───
    if is_rental:
        # Rental Context
        rental_charge = RentalChargeSettings.objects.filter(rental_user=request.user).first()
        target_fee = rental_charge.monthly_rent if rental_charge else Decimal('0.00')
        expected_acc = rental_charge.account_number[-4:] if rental_charge and rental_charge.account_number else None
        proofs = RentPaymentProof.objects.filter(rental_user=request.user).order_by('-created_at')
        settings_obj = rental_charge
    else:
        # Maintenance Context
        maintenance_settings = SocietyMaintenanceSettings.objects.filter(society_name=society_name).first()
        target_fee = request.user.get_maintenance_balance()
        expected_acc = maintenance_settings.expected_payee_account if maintenance_settings else None
        
        if request.user.role == 'secretary':
            proofs = PaymentProof.objects.filter(society_name=society_name).order_by('-created_at')
        else:
            proofs = PaymentProof.objects.filter(user=request.user).order_by('-created_at')
        settings_obj = maintenance_settings

    if request.method == 'POST' and request.FILES.get('proof_image'):
        proof_file = request.FILES.get('proof_image')
        
        if is_rental:
            proof = RentPaymentProof.objects.create(
                rental_user=request.user,
                owner=request.user.owner,
                proof_image=proof_file
            )
        else:
            proof = PaymentProof.objects.create(
                user=request.user,
                society_name=society_name,
                proof_image=proof_file
            )
        
        # --- AI Verification UI Logic (OCR) ---
        try:
            reader = get_ocr_reader()
            if not reader: raise Exception("OCR Reader Error")
            
            img = Image.open(proof.proof_image.path)
            results = reader.readtext(np.array(img))
            extracted_text = " ".join([res[1] for res in results])
            
            # --- OCR NORMALIZATION ---
            # OCR often confuses 'O' or 'o' with '0' (zero) in account numbers/amounts
            extracted_text = re.sub(r'([0-9])O', r'\1 0', extracted_text)
            extracted_text = extracted_text.replace('OO', '00').replace('oo', '00')
            
            # 1. Date Extraction
            date_patterns = [r'(\d{1,2}[/-]\d{1,2}[/-]\d{2,4})', r'(\d{1,2}\s+(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)[a-z]*\s+\d{2,4})']
            found_date = None
            date_range = (0,0)
            for pattern in date_patterns:
                match = re.search(pattern, extracted_text, re.IGNORECASE)
                if match:
                    date_str = match.group(1); date_range = match.span()
                    for fmt in ("%d/%m/%Y", "%d-%m-%Y", "%Y-%m-%d", "%d/%m/%y", "%d-%m-%y", "%d %b %Y"):
                        try: found_date = datetime.strptime(date_str, fmt).date(); break
                        except: continue
                    if found_date: break

            # 2. Transaction ID
            # Regex to find 10-30 alphanumeric characters that contain at least one DIGIT
            # and are NOT the word "Successful", "Pending", etc.
            txn_pattern = r'(?:Ref|UTR|Txn|Transaction|ID)[:\s]*([A-Z0-9]{10,35})'
            txn_matches = re.findall(txn_pattern, extracted_text, re.IGNORECASE)
            
            txn_id = None
            if txn_matches:
                # Filter out human-words like "SUCCESSFUL"
                valid_ids = []
                for tid in txn_matches:
                    upper_tid = tid.upper()
                    # Skip common labels read as IDs
                    if upper_tid in ["SUCCESSFUL", "TRANSACTION", "PENDING"]: continue
                    # Most IDs must have at least one digit
                    if any(char.isdigit() for char in upper_tid):
                        valid_ids.append(tid)
                
                if valid_ids:
                    # Prefer UTR-like (typically longer) IDs
                    txn_id = max(valid_ids, key=len)
            
            txn_range = (0,0)
            if txn_id:
                proof.transaction_id = txn_id
                # For masking later, find where it is
                m_txn = re.search(re.escape(txn_id), extracted_text)
                if m_txn: txn_range = m_txn.span()

            # 3. Amount Extraction: GREEDY TARGET SEARCH
            amount_found = False
            target_val = float(target_fee)
            target_str = str(int(target_val)) if target_val == int(target_val) else f"{target_val:.2f}"
            
            # Normalization helps here too (e.g. 1.OO -> 1.00)
            normalized_text = extracted_text.replace(' . ', '.').replace('. ', '.').replace(' .', '.')
            if target_str in normalized_text or str(int(target_val)) in normalized_text:
                proof.extracted_amount = Decimal(str(target_val))
                amount_found = True
            
            # --- RE-ENGINEERED ROBUST SCANNER (ALWAYS RUNS) ---
            potential_amounts = []
            candidates_str = ""
            transcript_str = extracted_text[:200].replace('\n', ' ')
            
            # Greedy search: Includes common OCR misreads of the currency symbol
            it = re.finditer(r'([0-9,]+\.[0-9]{2}|[₹rsFt]+[\s\.]*[0-9,]+|[0-9]{1,10})', normalized_text, re.IGNORECASE)
            
            for m in it:
                try:
                    num_raw = m.group(1).replace('₹', '').replace('Rs', '').replace('rs', '').replace('F', '').replace('t', '').replace(',', '').strip('.')
                    if not num_raw: continue
                    val = float(num_raw)
                    if val == 0: continue
                    s_idx = m.start(); e_idx = m.end()
                    
                    # WIDE RADAR (50 chars) to catch distant keywords
                    ctx_near = normalized_text[max(0, s_idx-50):min(len(normalized_text), e_idx+50)].lower()
                    
                    # MANDATORY NOISE BANNERS
                    if ':' in ctx_near or 'am' in ctx_near or 'pm' in ctx_near: continue
                    months = ['jan','feb','mar','apr','may','jun','jul','aug','sep','oct','nov','dec']
                    if any(mon in ctx_near for mon in months): continue
                    
                    # UI Context
                    has_symbol = any(s in m.group(1).lower() for s in ['₹', 'rs', 'f', 't'])
                    has_decimal = ('.' in m.group(1) and len(m.group(1).split('.')[-1]) == 2)
                    
                    # FILTER: Strictly skip metadata/years
                    if 2020 <= val <= 2030 or val > 1000000: continue
                    if any(k in ctx_near for k in ['@', '.com', 'okaxis', 'okicici', 'utr', 'ref']): 
                        if not has_symbol: continue # IDs are noise unless ₹ is present
                    
                    priority = 0
                    if has_symbol: priority += 100 
                    if has_decimal: priority += 50 
                    if s_idx < 350: priority += 40 
                    if any(w in ctx_near for w in ['paid', 'amount', 'total', 'credited', 'recived', 'received']): priority += 60
                    
                    # --- NAME PENALTY ---
                    name_noise = ['mr', 'ms', 'shri', 'smt', 'dr', 'shetty', 'recipient', 'sender', 'uday', 'shriraj']
                    if any(n in ctx_near for n in name_noise) and not has_symbol:
                        priority -= 250 # CRUSH names to the bottom

                    potential_amounts.append({'val': val, 'priority': priority})
                except: continue
            
            if potential_amounts:
                potential_amounts.sort(key=lambda x: x['priority'], reverse=True)
                candidates_str = ", ".join([f"₹{p['val']} (P:{p['priority']})" for p in potential_amounts[:5]])
                
                found_target = False
                for p_obj in potential_amounts:
                    if abs(p_obj['val'] - target_val) < 0.05:
                        proof.extracted_amount = Decimal(str(p_obj['val']))
                        amount_found = True; found_target = True; break
                
                if not found_target:
                    proof.extracted_amount = Decimal(str(potential_amounts[0]['val']))
                    amount_found = True

            # 4. Recipient Check (Account Digits)
            digit_found = None
            raw_text_normalized = extracted_text.replace('O', '0').replace('o', '0').replace(' ', '')
            
            # HIGHEST PRIORITY: If our expected 4 digits exist ANYWHERE (after normalization)
            if expected_acc:
                if str(expected_acc) in raw_text_normalized:
                    digit_found = str(expected_acc)

            # SECOND PRIORITY: Anchor search near "Credited to"
            if not digit_found:
                text_lower = extracted_text.lower()
                anchor_idx = text_lower.find("credited")
                if anchor_idx != -1:
                    after_anchor = extracted_text[anchor_idx:anchor_idx+120]
                    # Look for XXXXX + 4 chars (allow O)
                    m = re.search(r'[X*]+[\s]*([0-9Oo]{4})', after_anchor, re.IGNORECASE)
                    if m: digit_found = m.group(1).replace('O', '0').replace('o', '0')
                    else:
                        # Just any 4 digits near it
                        m2 = re.findall(r'([0-9]{4})', after_anchor)
                        for d in m2:
                            if not (2020 <= int(d) <= 2030): digit_found = d; break

            if digit_found:
                proof.extracted_account_digits = digit_found[-4:]
            
            # --- VERIFICATION ---
            is_valid = True; reason = ""
            today_date = date.today()
            debug_info = f" | CANDIDATES: {candidates_str} | RAW OCR: {transcript_str}" if potential_amounts else f" | RAW OCR: {transcript_str}"

            if proof.transaction_id:
                already_exists = (
                    PaymentProof.objects.filter(transaction_id=proof.transaction_id).exclude(id=proof.id).exists() or
                    RentPaymentProof.objects.filter(transaction_id=proof.transaction_id).exclude(id=proof.id).exists()
                )
                if already_exists:
                    is_valid = False; reason += "Duplicate Transaction ID detected. "
            
            if found_date:
                proof.extracted_date = found_date
                if found_date != today_date:
                    is_valid = False; reason += f"Date Mismatch (Detected: {found_date}, Today: {today_date}). "
            else:
                is_valid = False; reason += "Date not detected. "

            if expected_acc:
                if not proof.extracted_account_digits or str(proof.extracted_account_digits) != str(expected_acc):
                    is_valid = False; reason += f"Account Mismatch (Last 4: {proof.extracted_account_digits or 'None'}). "

            if not amount_found or not found_target:
                is_valid = False
                reason += f"Amount Mismatch.{debug_info}"

            if is_valid:
                proof.status = 'verified'
                messages.success(request, f"✅ Verified: Matches Today's Date, Account Number, and Amount (₹{proof.extracted_amount}).")
            else:
                proof.status = 'pending'
                messages.warning(request, f"⚠️ Verification failed: {reason}")
            
            proof.save()
            
        except Exception as e:
            messages.warning(request, f'Proof uploaded, but AI verification skipped. ({str(e)})')
            
        return redirect('maintenance')

    return render(request, 'core/maintenance.html', {
        'is_rental': is_rental,
        'proofs': proofs,
        'society_name': society_name,
        'settings': settings_obj,
        'target_fee': target_fee,
    })

@login_required
def delete_payment_proof(request, proof_id):
    from .models import PaymentProof, RentPaymentProof
    is_rental = (request.user.role == 'resident' and request.user.resident_role == 'rental')
    
    if is_rental:
        proof = RentPaymentProof.objects.filter(id=proof_id, rental_user=request.user).first()
    else:
        proof = PaymentProof.objects.filter(id=proof_id).first()
    
    if not proof:
        messages.error(request, "Proof not found.")
        return redirect('maintenance')
    
    # Check permission: Only owner or secretary can delete
    # For rent: only tenant or their owner
    can_delete = False
    if is_rental and (proof.rental_user == request.user or proof.owner == request.user):
        can_delete = True
    elif not is_rental and (proof.user == request.user or request.user.role == 'secretary'):
        can_delete = True

    if can_delete:
        proof.delete()
        messages.success(request, "Payment proof deleted successfully.")
    else:
        messages.error(request, "You do not have permission to delete this proof.")
        
    return redirect('maintenance')


@login_required
def generate_proof_receipt_pdf(request, proof_id):
    from .models import PaymentProof
    from django.http import HttpResponse
    from django.template.loader import get_template
    from xhtml2pdf import pisa

    proof = PaymentProof.objects.filter(id=proof_id, user=request.user).first()

    if not proof:
        messages.error(request, "Receipt not found.")
        return redirect('maintenance')

    if proof.status not in ['verified', 'approved']:
        messages.error(request, "Receipt is only available for verified payments.")
        return redirect('maintenance')

    template = get_template('core/proof_receipt_pdf.html')
    html = template.render({'proof': proof})

    response = HttpResponse(content_type='application/pdf')
    response['Content-Disposition'] = f'attachment; filename="receipt_proof_{proof.id}.pdf"'

    pisa_status = pisa.CreatePDF(html, dest=response)
    if pisa_status.err:
        return HttpResponse('Error generating receipt. <pre>' + html + '</pre>')
    return response
